package myshop.opboard;


public class OpBoardDTO {
	int op_idx;
	String op_id; 
	String op_phone;
	String op_title;
	String op_content;
	String op_ip;
	String op_email;
	String op_answer;
	
	public int getOp_idx() {
		return op_idx;
	}
	public void setOp_idx(int op_idx) {
		this.op_idx = op_idx;
	}
	public String getOp_id() {
		return op_id;
	}
	public void setOp_id(String op_id) {
		this.op_id = op_id;
	}
	public String getOp_phone() {
		return op_phone;
	}
	public void setOp_phone(String op_phone) {
		this.op_phone = op_phone;
	}
	public String getOp_title() {
		return op_title;
	}
	public void setOp_title(String op_title) {
		this.op_title = op_title;
	}
	public String getOp_content() {
		return op_content;
	}
	public void setOp_content(String op_content) {
		this.op_content = op_content;
	}
	public String getOp_ip() {
		return op_ip;
	}
	public void setOp_ip(String op_ip) {
		this.op_ip = op_ip;
	}
	public String getOp_email() {
		return op_email;
	}
	public void setOp_email(String op_email) {
		this.op_email = op_email;
	}
	public String getOp_answer() {
		return op_answer;
	}
	public void setOp_answer(String op_answer) {
		this.op_answer = op_answer;
	}
	
}